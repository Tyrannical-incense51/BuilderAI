# ── Scanner ───────────────────────────────────────────────────────────────────

def scan_missing_ui_imports(files: dict) -> set[str]:
    """
    Parse all .tsx/.ts files for '@/components/ui/X' imports.
    Return set of X names whose files are NOT already in `files`.
    """
    import_pattern = re.compile(r'''from\s+['"]@/components/ui/([^'"]+)['"]''')
    needed: set[str] = set()

    for content in files.values():
        for match in import_pattern.finditer(content):
            component_name = match.group(1).lower()
            needed.add(component_name)

    present: set[str] = set()
    for path in files:
        if "components/ui/" in path:
            name = path.split("components/ui/")[-1].replace(".tsx", "").replace(".ts", "")
            present.add(name.lower())

    return needed - present


# ── Pipeline node ─────────────────────────────────────────────────────────────

def ensure_ui_components(state: BuilderState) -> BuilderState:
    """
    Scans all integrated files for @/components/ui/* imports.
    Injects hardcoded shadcn/ui templates for any that are missing.
    Also ensures lib/utils.ts exists (required by all components).
    Zero LLM calls — 100% reliable.
    """
    integrated_files = state.get("integrated_files") or {}
    if not integrated_files:
        return state

    updated = dict(integrated_files)
    injected: list[str] = []

    # Always ensure src/lib/utils.ts exists (required by all shadcn/ui components)
    if "src/lib/utils.ts" not in updated:
        updated["src/lib/utils.ts"] = UI_TEMPLATES["utils"]
        injected.append("src/lib/utils.ts")

    # Always overwrite src/components/ui/* with trusted templates — LLM-generated ones may be broken
    for name, template in UI_TEMPLATES.items():
        if name == "utils":
            continue  # utils handled above
        path = f"src/components/ui/{name}.tsx"
        updated[path] = template
        injected.append(path)

    events = []
    if injected:
        events.append({
            "type": "agent_complete",
            "agent": "ui_components",
            "message": f"Injected {len(injected)} missing UI files: {', '.join(injected)}",
        })

    return {**state, "integrated_files": updated, "events": events}
